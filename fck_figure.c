#include "fillit.h"

int		first_tetr(int *x, int *y)
{
	if (x[1] == -1 && y[1] == 0)
	{
		if (x[2] == -1 && y[2] == -1)
		{
			if (x[3] == -2 && y[3] == -1)
				return (5);
			else if (x[3] == -1 && y[3] == -2)
				return (17);
			else if (x[3] == -2 && y[3] == 0)
				return (9);
		}
		else if (x[2] == -2 && y[2] == 0)
		{
			if (x[3] == -3 && y[3] == 0)
				return (1);
			else if (x[3] == -2 && y[3] == -1)
				return (12);
		}
		else if ((x[2] == -2 && y[2] == 1) && (x[3] == -2 && y[3] == 0))
			return (16);
	}
	return (0);
}

int		second_tetr(int *x, int *y)
{
	if (x[1] == 0 && y[1] == -1)
	{
		if (x[2] == 0 && y[2] == -2)
		{
			if (x[3] == 0 && y[3] == -3)
				return (2);
			else if (x[3] == -1 && y[3] == -1)
				return (10);
			else if (x[3] == -1 && y[3] == 0)
				return (13);
			else if (x[3] == -1 && y[3] == -2)
				return (19);
		}
		else if (x[2] == -1 && y[2] == 0)
		{
			if (x[3] == -1 && y[3] == -1)
				return (3);
			else if (x[3] == -2 && y[3] == 0)
				return (18);
		}
	}
	return (0);
}

int		third_tetr(int *x, int *y)
{
	if (x[1] == -1 && y[1] == 1)
	{
		if ((x[2] == -1 && y[2] == 0) && (x[3] == -2 && y[3] == 1))
			return (7);
		else if ((x[2] == -1 && y[2] == 0) && (x[3] == -1 && y[3] == -1))
			return (8);
		else if (x[2] == -1 && y[2] == 0 && x[3] == -2 && y[3] == 0)
			return (11);
	}
	if (x[1] == 0 && y[1] == -1)
	{
		if ((x[2] == -1 && y[2] == 1) && (x[3] == -1 && y[3] == 0))
			return (4);
		else if ((x[2] == -1 && y[2] == -1) && (x[3] == -1 && y[3] == -2))
			return (6);
		else if ((x[2] == -1 && y[2] == -1) && (x[3] == -2 && y[3] == -1))
			return (14);
	}
	if ((x[1] == -1 && y[1] == 2) && ((x[2] == -1 && y[2] == 1) &&
				(x[3] == -1 && y[3] == 0)))
		return (15);
	return (0);
}
